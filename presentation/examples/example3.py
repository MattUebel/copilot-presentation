# Example 3: Demonstrating a feature of GitHub Copilot Workspace

def square(number):
    """
    Function to calculate the square of a number.
    """
    return number ** 2

def cube(number):
    """
    Function to calculate the cube of a number.
    """
    return number ** 3

def main():
    """
    Main function to demonstrate the square and cube functions.
    """
    num = 3
    print(f"The square of {num} is {square(num)}")
    print(f"The cube of {num} is {cube(num)}")

if __name__ == "__main__":
    main()
